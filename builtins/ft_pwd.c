#include "inc.h"

int	ft_pwd(void)
{
	char	*str;

	str = getcwd(NULL, 0);
	if (str)
	{
		write(1, str, ft_strlen(str));
		write(1, "\n", 1);
		free(str);
		return (0);
	}
	return (-1);
}
